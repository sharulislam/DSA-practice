public class bitwise_operators {

    public static void main(String[] args){
        System.out.println("in binary AND :"+(5&6));
        System.out.println("in binary OR :"+ (5|6));
        System.out.println("in binary XOR :"+(5^6));
        System.out.println("in Binary 1's complement : "+(~5));
        System.out.println("in binary NOT : "+(~0));
        System.out.println("Binary left shift :"+(5<<2));
        System.out.println("Binary right shift : "+(6>>1));
    }
}